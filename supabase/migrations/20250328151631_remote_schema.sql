    UPDATE public.user
    SET normal = normal + 1, score = score + 50
    WHERE wallet_address = p_creator;
  ELSIF p_level = 3 THEN
    UPDATE public.user
    SET hard = hard + 1, score = score + 200
    WHERE wallet_address = p_creator;
  ELSIF p_level = 4 THEN
    UPDATE public.user
    SET extreme = extreme + 1, score = score + 600
    WHERE wallet_address = p_creator;
  END IF;
END;
$$;


